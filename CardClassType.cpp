#include "CardClassType.h"

//----------------------------------------------------------------------------------------------------------------------
// Default Constructor
//----------------------------------------------------------------------------------------------------------------------
Card::Card()
{
  suit = hearts;
  rank = 0;
}

//----------------------------------------------------------------------------------------------------------------------
// Overloaded Constructor
//----------------------------------------------------------------------------------------------------------------------
Card::Card(suits tempSuit, int tempRank)
{
  suit = tempSuit;
  rank = tempRank;

}

//----------------------------------------------------------------------------------------------------------------------
// SetRank - Sets a rank
//----------------------------------------------------------------------------------------------------------------------
void Card::SetRank(int tempRank)
{
  // setting the rank
  rank = tempRank;
}

//----------------------------------------------------------------------------------------------------------------------
// SetSuit - Sets a suit
//----------------------------------------------------------------------------------------------------------------------
void Card::SetSuit(suits tempSuit)
{
  // setting the suit
  suit = tempSuit;
}

//----------------------------------------------------------------------------------------------------------------------
// PrintCard - Prints One Card
//----------------------------------------------------------------------------------------------------------------------
void Card::PrintCard()
{
  char printChar;

  if (rank >= 2 && rank <= 9)
  {
    cout << "Card Is a  " << rank << " of " << (char)suit << endl;
  }
  else if (rank == 10)
  {
    cout << "Card Is a " << rank << " of " << (char)suit << endl;
  }
  else
  {
    switch (rank)
    {
    case 11:
      printChar = 'J';
      break;
    case 12:
      printChar = 'Q';
      break;
    case 13:
      printChar = 'K';
      break;
    default:
      printChar = 'A';
      break;
    }
    cout << "Card Is a  " << printChar << " of " << (char)suit << endl;
  }
}

bool Card::IsCardValid()
{
  return (rank != 0);
}

